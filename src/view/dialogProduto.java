    private Produto createObject(){
        return new Produto(
                textCod.getText().isEmpty() ? 0 : Integer.parseInt(textCod.getText()),
                textNome.getText(),
                (int)textQuantidade.getValue(),
                (double)textValor.getValue()
        );
    }
    
    private void loadTable(){
        tableDados.setModel(new MyTableModel(Produto.class, daoProduto.read(), tableDados));
    }
    
    private void loadTable(String filtro) {
        tableDados.setModel(new MyTableModel(Produto.class, daoProduto.read(filtro), tableDados));
    }

