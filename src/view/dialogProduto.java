    private void tableDadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableDadosMouseClicked
        if (evt.getClickCount()==2){
            int codigo = Integer.parseInt(tableDados.getValueAt(tableDados.getSelectedRow(), 0) + "");
            Produto produto = daoProduto.read(Produto.class, codigo);
            this.fillComponents(produto);
        }
    }//GEN-LAST:event_tableDadosMouseClicked

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if (textNome.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Nome obrigatÃ³rio!");
            textNome.requestFocus();
            return;
        }
        double valor = 0;
        
        int quantidade = (int)textQuantidade.getValue();
        double valorProduto =  (double)textValor.getValue();
        
        if (quantidade <= 0){
            JOptionPane.showMessageDialog(null, "Quantidade nÃ£o pode ser menor ou igual a zero");
            textQuantidade.requestFocus();
            return;
        }
        
        if (valorProduto <= 0){
            JOptionPane.showMessageDialog(null, "Valor nÃ£o pode ser menor ou igual a zero");
            textValor.requestFocus();
            return;
        }
        Produto produto = this.createObject();
        try {
            if (textCod.getText().isEmpty()) {
                daoProduto.create(produto);
            }else{
                daoProduto.update(produto);
            }
            JOptionPane.showMessageDialog(null, "Gravado com sucesso!");
            this.cleanComponents();
            this.loadTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao gravar!", "Erro", JOptionPane.ERROR_MESSAGE);
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

