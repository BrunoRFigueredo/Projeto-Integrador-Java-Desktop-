    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
        if (textCod.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Selecione um registro!");
           return;
        }
        if (JOptionPane.showConfirmDialog(null, "Confirma exclusÃ£o?") != 0) {
            return;
        }
        Produto produto = daoProduto.read(Produto.class, 
                                   Integer.parseInt(textCod.getText()));
        try {
            daoProduto.delete(produto);
            JOptionPane.showMessageDialog(null, "Produto removido com sucesso!");
            this.cleanComponents();
            this.loadTable();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao remover!", "Erro", JOptionPane.ERROR_MESSAGE);
            System.out.println(e.getMessage());
        }
        
    }//GEN-LAST:event_btnRemoveActionPerformed

